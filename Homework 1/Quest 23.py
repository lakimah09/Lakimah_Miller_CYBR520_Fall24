categories = ['A', 'B', 'C', 'D']
values = [4, 7, 2, 6]

# Create the bar chart
plt.bar(categories, values)

# Add labels and title
plt.xlabel('Categories')
plt.ylabel('Values')
plt.title('Example Bar Chart')

# Display the chart
plt.show()

#Characteristics of data that makes a bar chart suitable is: Single or multiple data points, categorical or discrete data, ordinal data, and small to medium size data